package com.company;

public class Java_07_4_switchcase {

    public static void main(String[] args) {

        int num = 1;

        switch (num) {
            case 1:
                System.out.println("1 is pressed ");
                break;
            case 2:
                System.out.println("2 is pressed");
                break;
            case 3:
                System.out.println("3 is pressed");
                break;
            default:
                System.out.println("other than 1 ,2 ,3 is pressed");
        }

    }
}
